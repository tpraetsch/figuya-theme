def emit_gtk_named_theme(pd, pl):
    # A named, switchable GTK3 theme (shows up in nwg-look / gtk-theme-name).
    # Self-contained: bundles its own colour css and @imports it relative, plus
    # Adwaita's stylesheet from the gresource. GTK3-only by design (libadwaita
    # ignores named themes and reads ~/.config/gtk-4.0/gtk.css).
    base = "gtk/themes/figuya"
    write(f"{base}/index.theme",
          "[Desktop Entry]\n"
          "Type=X-GNOME-Metatheme\n"
          "Name=figuya\n"
          "Comment=Generated by figuya generate.py\n"
          "Encoding=UTF-8\n\n"
          "[X-GNOME-Metatheme]\n"
          "GtkTheme=figuya\n")
    write(f"{base}/gtk-3.0/colors-light.css", _gtk3_css(pl))
    write(f"{base}/gtk-3.0/colors-dark.css", _gtk3_css(pd))
    for css, adwaita, colors in (
        ("gtk.css", "gtk-contained.css", "colors-light.css"),
        ("gtk-dark.css", "gtk-contained-dark.css", "colors-dark.css"),
    ):
        write(f"{base}/gtk-3.0/{css}",
              "/* figuya — generated wrapper theme: Adwaita base + figuya colours.\n"
              "   Edit palette.toml + rerun generate.py, not this. */\n"
              f'@import url("resource:///org/gtk/libgtk/theme/Adwaita/{adwaita}");\n'
              f'@import url("{colors}");\n')


def emit_fish(pd, pl):
    # Prompt palette globals read by fish_right_prompt.fish via indirection as
    # forte_<mode>_<role>. Role names kept for drop-in compatibility with the
    # existing prompt (accent/error/warn/caution/link/info/mute).
    lines = ["# figuya prompt palette — generated by figuya generate.py.",
             "# fish_right_prompt reads forte_{$theme_mode}_<role> by indirection.", ""]
    for p in (pd, pl):
        s = _slot(p)
        v = p["variant"]
        for role in ("accent", "error", "warn", "caution", "link", "info"):
            lines.append(f"set -g forte_{v}_{role} {s[role]}")
        lines.append(f"set -g forte_{v}_mute {p['dim']}")
        lines.append("")
    write("fish/figuya-theme.fish", "\n".join(lines))


# ---- preview ----------------------------------------------------------------
def preview(pd, pl):
    def row(p):
        out = f"\x1b[48;2;{';'.join(map(str, _hx(p['bg'])))}m "
        for role in BRAND + ("text",):
            r, g, b = _hx(p[role])
            out += f"\x1b[38;2;{r};{g};{b}m{role[:4]} "
        return out + "\x1b[0m"
    print("dark : " + row(pd))
    print("light: " + row(pl))


# ---- main -------------------------------------------------------------------
def main():
    with open(os.path.join(HERE, "palette.toml"), "rb") as f:
        cfg = tomllib.load(f)
    pd, pl = resolve(cfg, "dark"), resolve(cfg, "light")

    # editors
    emit_nvim(pd, pl)
    emit_vscode_package()
    # cli (cross-variant)
    emit_delta(pd, pl)
    emit_zellij(pd, pl)
    # desktop / rice (cross-variant)
    emit_fish(pd, pl)
    emit_gtk_named_theme(pd, pl)
    emit_foot_combined(pd, pl)
    for p in (pd, pl):
        # terminals
        emit_alacritty(p)
        emit_kitty(p)
        emit_wezterm(p)
        emit_foot(p)
        emit_ghostty(p)
        # editors
        emit_helix(p)
        emit_vscode_theme(p)
        # cli
        emit_bat(p)
        emit_fzf(p)
        emit_tmux(p)
        # desktop / rice
        emit_waybar_css(p)
        emit_fuzzel(p)
        emit_mako(p)
        emit_hyprland(p)
        emit_hyprlock(p)
        emit_gtk3(p)
        emit_gtk4(p)

    print(f"figuya: wrote {len(_written)} files to dist/")
    for rel in sorted(_written):
        print("  dist/" + rel)
    if "--preview" in sys.argv:
        print()
        preview(pd, pl)


if __name__ == "__main__":
    main()
