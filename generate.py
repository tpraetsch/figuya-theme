def emit_fuzzel(p):
    # The selected row's text is base-100 (the bg), so the selection fill must
    # clear the contrast floor against it: prefer the vivid brand orange, and fall
    # back to the contrast-floored orange where raw is too light (e.g. cream text
    # on the light bg, where vivid orange would only reach ~2.8:1).
    sel = p["primary_raw"]
    if contrast(_hx(p["bg"]), _hx(sel)) < 4.5:
        sel = p["primary"]
    colors = f"""
[colors]
background={p['bg']}ff
text={p['text']}ff
prompt={p['primary']}ff
placeholder={p['dim']}ff
input={p['text']}ff
match={p['error']}ff
selection={sel}ff
selection-text={p['bg']}ff
selection-match={p['bg']}ff
counter={p['dim']}ff
border={p['text']}ff
"""
    # Default prompt = an accent search glyph (nf-md-magnify). Built from its
    # codepoint so the PUA char survives editing. Menus that pass their own
    # --prompt (the launcher, sensors, …) override this; the prompt *color* above
    # still applies to them.
    layout = _FUZZEL_LAYOUT.replace("@@SEARCH@@", chr(0xF0349) + "  ")
    write(f"fuzzel/figuya-{p['variant']}.ini",
          f"# figuya ({p['variant']}) — generated by generate.py.\n"
          + layout + colors)


