def emit_nvim(pd, pl):
    def block(p, indent):
        lines = []
        for grp, fgc, bgc, opts in _hl_groups(p):
            parts = []
            if fgc:
                parts.append(f'fg = "#{fgc}"')
            if bgc:
                parts.append(f'bg = "#{bgc}"')
            if opts.get("sp"):
                parts.append(f'sp = "#{opts["sp"]}"')
            for k in ("bold", "italic", "underline", "undercurl"):
                if opts.get(k):
                    parts.append(f"{k} = true")
            lines.append(f'{indent}set(0, "{grp}", {{ {", ".join(parts)} }})')
        return "\n".join(lines)

    body = f"""-- {GEN}
-- Drop this file in a directory on your 'runtimepath' under colors/ (e.g.
-- ~/.config/nvim/colors/figuya.lua), then `:colorscheme figuya`. The variant
-- follows `:set background=light|dark`.
vim.cmd("highlight clear")
if vim.fn.exists("syntax_on") == 1 then vim.cmd("syntax reset") end
vim.o.termguicolors = true
vim.g.colors_name = "figuya"

local set = vim.api.nvim_set_hl
if vim.o.background == "light" then
{block(pl, "  ")}
else
{block(pd, "  ")}
end
"""
    write("nvim/colors/figuya.lua", body)


