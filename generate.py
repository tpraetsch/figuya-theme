# ---- preview ----------------------------------------------------------------
def preview(pd, pl):
    def row(p):
        out = f"\x1b[48;2;{';'.join(map(str, _hx(p['bg'])))}m "
        for role in BRAND + ("text",):
            r, g, b = _hx(p[role])
            out += f"\x1b[38;2;{r};{g};{b}m{role[:4]} "
        return out + "\x1b[0m"
    print("dark : " + row(pd))
    print("light: " + row(pl))


# ---- main -------------------------------------------------------------------
def main():
    with open(os.path.join(HERE, "palette.toml"), "rb") as f:
        cfg = tomllib.load(f)
    pd, pl = resolve(cfg, "dark"), resolve(cfg, "light")

    # editors
    emit_nvim(pd, pl)
    emit_vscode_package()
    # cli (cross-variant)
    emit_delta(pd, pl)
    emit_zellij(pd, pl)
    for p in (pd, pl):
        # terminals
        emit_alacritty(p)
        emit_kitty(p)
        emit_wezterm(p)
        emit_foot(p)
        emit_ghostty(p)
        # editors
        emit_helix(p)
        emit_vscode_theme(p)
        # cli
        emit_bat(p)
        emit_fzf(p)
        emit_tmux(p)

    print(f"figuya: wrote {len(_written)} files to dist/")
    for rel in sorted(_written):
        print("  dist/" + rel)
    if "--preview" in sys.argv:
        print()
        preview(pd, pl)


if __name__ == "__main__":
    main()
