def emit_bat(p):
    # Minimal .tmTheme (Sublime plist). Covers the scopes bat highlights most.
    def d(scope, name, fg, style=""):
        extra = f"\n        <key>fontStyle</key>\n        <string>{style}</string>" if style else ""
        return f"""      <dict>
        <key>name</key>
        <string>{name}</string>
        <key>scope</key>
        <string>{scope}</string>
        <key>settings</key>
        <dict>
          <key>foreground</key>
          <string>#{fg}</string>{extra}
        </dict>
      </dict>"""

    items = "\n".join([
        d("comment", "Comment", p["dim"], "italic"),
        d("string", "String", p["success"]),
        d("constant.numeric", "Number", p["tertiary"]),
        d("constant.language", "Built-in constant", p["tertiary"]),
        d("keyword, storage", "Keyword", p["secondary"]),
        d("keyword.operator", "Operator", p["primary"]),
        d("entity.name.function, support.function", "Function", p["info"]),
        d("entity.name.type, support.type, storage.type", "Type", p["warning"]),
        d("entity.name.tag", "Tag", p["secondary"]),
        d("entity.other.attribute-name", "Attribute", p["info"]),
        d("variable", "Variable", p["text"]),
        d("punctuation", "Punctuation", p["dim"]),
        d("markup.heading", "Heading", p["primary"], "bold"),
        d("markup.inserted", "Diff inserted", p["success"]),
        d("markup.deleted", "Diff deleted", p["error"]),
        d("markup.changed", "Diff changed", p["info"]),
    ])
    body = f"""<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<!-- {GEN} -->
<plist version="1.0">
<dict>
  <key>name</key>
  <string>figuya-{p['variant']}</string>
  <key>settings</key>
  <array>
    <dict>
      <key>settings</key>
      <dict>
        <key>background</key>
        <string>#{p['bg']}</string>
        <key>foreground</key>
        <string>#{p['text']}</string>
        <key>caret</key>
        <string>#{p['primary_raw']}</string>
        <key>lineHighlight</key>
        <string>#{p['hl_med']}</string>
        <key>selection</key>
        <string>#{p['hl_med']}</string>
      </dict>
    </dict>
{items}
  </array>
</dict>
</plist>
"""
    write(f"bat/figuya-{p['variant']}.tmTheme", body)


def emit_delta(pd, pl):
    def section(p):
        v = p["variant"]
        return f"""[delta "figuya-{v}"]
    syntax-theme = figuya-{v}
    minus-style = syntax "#{mix(p['error'], p['bg'], 0.80)}"
    minus-emph-style = syntax "#{mix(p['error'], p['bg'], 0.62)}"
    plus-style = syntax "#{mix(p['success'], p['bg'], 0.80)}"
    plus-emph-style = syntax "#{mix(p['success'], p['bg'], 0.62)}"
    line-numbers-minus-style = "#{p['error']}"
    line-numbers-plus-style = "#{p['success']}"
    line-numbers-zero-style = "#{p['dim']}"
    hunk-header-decoration-style = "#{p['dim']}" box
    file-style = "#{p['primary_raw']}" bold
    file-decoration-style = "#{p['overlay']}" ul
"""
    body = (f"# {GEN}\n"
            "# Activate with:  git config include.path "
            "<path>/figuya.gitconfig  and  delta.features figuya-dark\n\n"
            + section(pd) + "\n" + section(pl))
    write("delta/figuya.gitconfig", body)


