#!/usr/bin/env python3
"""figuya theme generator.

Reads palette.toml, derives a WCAG-floored colour set for the light and dark
variants (brand hues fixed, contrast computed not hand-tuned), and writes a
self-contained theme file for each supported app into ./dist.

    python3 generate.py [--preview]

Hue-preserving floor: on a dark background a too-dim colour is lifted toward
white; on a light background it is darkened toward black. Either way the hue is
held and only lightness moves, so every accent clears its WCAG floor against the
background it actually sits on, in both variants.

Nothing here touches your live config — every output lands under ./dist. See the
README for where each port file goes.
"""
