    a = _ansi16(p)
    tnames = ["Black", "Red", "Green", "Yellow", "Blue", "Magenta", "Cyan", "White"]
    for i, n in enumerate(tnames):
        colors[f"terminal.ansi{n}"] = "#" + a[i]
        colors[f"terminal.ansiBright{n}"] = "#" + a[8 + i]

    def tok(scope, fg, style=None):
        s = {"foreground": "#" + fg}
        if style:
            s["fontStyle"] = style
        return {"scope": scope, "settings": s}

    token_colors = [
        tok("comment", p["dim"], "italic"),
        tok("string", p["success"]),
        tok("constant.numeric", p["tertiary"]),
        tok("constant.language", p["tertiary"]),
        tok("constant.character, constant.other", p["tertiary"]),
        tok("variable", p["text"]),
        tok("variable.language, variable.other.constant", p["primary"]),
        tok("keyword, storage.type, storage.modifier", p["secondary"]),
        tok("keyword.operator", p["primary"]),
        tok("entity.name.function, support.function", p["info"]),
        tok("entity.name.type, support.type, entity.name.class", p["warning"]),
        tok("entity.name.tag", p["secondary"]),
        tok("entity.other.attribute-name", p["info"]),
        tok("variable.parameter", p["text"]),
        tok("punctuation, meta.brace", p["dim"]),
        tok("markup.heading", p["primary"], "bold"),
        tok("markup.bold", p["text"], "bold"),
        tok("markup.italic", p["text"], "italic"),
        tok("markup.inline.raw, markup.fenced_code", p["success"]),
        tok("markup.underline.link", p["info"], "underline"),
        tok("invalid", p["error"]),
    ]
    theme = {
        "name": name,
        "type": p["variant"],
        "colors": colors,
        "tokenColors": token_colors,
    }
    write(f"vscode/themes/{name}-color-theme.json", json.dumps(theme, indent=2) + "\n")


