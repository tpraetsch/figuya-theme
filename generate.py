HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "dist")

# Fixed light/dark poles used to pick a readable foreground on an accent fill.
INK = "2d2220"
PAPER = "fffaf3"


# ---- colour maths -----------------------------------------------------------
def _lin(c):
    c /= 255
    return c / 12.92 if c <= 0.03928 else ((c + 0.055) / 1.055) ** 2.4


def _lum(rgb):
    r, g, b = rgb
    return 0.2126 * _lin(r) + 0.7152 * _lin(g) + 0.0722 * _lin(b)


def _hx(h):
    h = h.lstrip("#")
    return tuple(int(h[i:i + 2], 16) for i in (0, 2, 4))


def _hs(rgb):
    return "%02x%02x%02x" % rgb


def contrast(fg, bg):
    a, b = sorted((_lum(fg), _lum(bg)), reverse=True)
    return (a + 0.05) / (b + 0.05)


def floor(fg_hex, bg_hex, ratio):
    """Lift (dark bg) or darken (light bg) fg until contrast >= ratio, hue held."""
    fg, bg = _hx(fg_hex), _hx(bg_hex)
    if contrast(fg, bg) >= ratio:
        return _hs(fg)
    toward = (255, 255, 255) if _lum(bg) < 0.5 else (0, 0, 0)
    lo, hi = 0.0, 1.0
    for _ in range(50):
        t = (lo + hi) / 2
        cand = tuple(round(fg[i] + (toward[i] - fg[i]) * t) for i in range(3))
        if contrast(cand, bg) >= ratio:
            hi = t
        else:
            lo = t
    return _hs(tuple(round(fg[i] + (toward[i] - fg[i]) * hi) for i in range(3)))


def mix(h1, h2, t):
    a, b = _hx(h1), _hx(h2)
    return _hs(tuple(round(a[i] + (b[i] - a[i]) * t) for i in range(3)))


def on(hexv):
    """Readable foreground (ink or paper) for a fill of colour `hexv`."""
    return INK if contrast(_hx(INK), _hx(hexv)) >= contrast(_hx(PAPER), _hx(hexv)) else PAPER


# ---- palette resolution -----------------------------------------------------
BRAND = ("primary", "secondary", "tertiary", "info", "success", "warning", "error")


def resolve(cfg, variant):
    """Flat dict of hex strings (no '#') for one variant."""
    v = cfg["variant"][variant]
    fl = cfg["floors"]
    bg = v["bg"]
    p = {
        "variant": variant,
        "bg": bg,
        "surface": v["surface"],
        "overlay": v["overlay"],
        "hl_med": v["hl_med"],
        "text": floor(v["text"], bg, fl["text"]),
        "dim": floor(v["dim"], bg, fl["dim"]),
    }
    # Brand accents: floored for legible text (`role`) + raw brand hex for
    # large/non-text UI accents (`role_raw`: cursors, borders, signs).
    for role in BRAND:
        raw = cfg["brand"][role]
        p[role] = floor(raw, bg, fl["accent"])
        p[role + "_raw"] = raw
    # Terminal ANSI hues. Values in [ansi] are either a brand-role name or a hex.
    for slot, ref in cfg["ansi"].items():
        raw = cfg["brand"][ref] if ref in cfg["brand"] else ref
        p["ansi_" + slot] = floor(raw, bg, fl["accent"])
    # ANSI black/white poles, ordered 0 < 8 < 7 < 15 by luminance in both modes.
    if variant == "dark":
        p["c0"], p["c8"] = p["overlay"], p["dim"]
        p["c7"], p["c15"] = mix(p["dim"], p["text"], 0.5), p["text"]
    else:
        p["c0"], p["c8"] = p["text"], p["dim"]
        p["c7"], p["c15"] = mix(p["overlay"], p["surface"], 0.5), p["surface"]
    return p


# ---- io ---------------------------------------------------------------------
_written = []


def write(relpath, body):
    path = os.path.join(OUT, relpath)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(body)
    _written.append(relpath)


def H(p, key):
    return "#" + p[key]


GEN = "figuya — generated by generate.py from palette.toml. Do not hand-edit."


# ── Terminals ────────────────────────────────────────────────────────────────
def _ansi16(p):
    """The 16 ANSI colours (no '#'), in index order."""
    hues = ("ansi_red", "ansi_green", "ansi_yellow",
            "ansi_blue", "ansi_magenta", "ansi_cyan")
    normal = [p["c0"]] + [p[h] for h in hues] + [p["c7"]]
    bright = [p["c8"]] + [p[h] for h in hues] + [p["c15"]]
    return normal + bright


def emit_alacritty(p):
    a = _ansi16(p)
    names = ["black", "red", "green", "yellow", "blue", "magenta", "cyan", "white"]
    body = [f"# {GEN}", "[colors.primary]",
            f'background = "0x{p["bg"]}"', f'foreground = "0x{p["text"]}"', "",
            "[colors.cursor]", f'cursor = "0x{p["primary_raw"]}"',
            f'text = "0x{p["bg"]}"', "",
            "[colors.selection]", f'background = "0x{p["hl_med"]}"',
            f'text = "0x{p["text"]}"', ""]
    for label, off in (("normal", 0), ("bright", 8)):
        body.append(f"[colors.{label}]")
        for i, n in enumerate(names):
            body.append(f'{n} = "0x{a[off + i]}"')
        body.append("")
    write(f"alacritty/figuya-{p['variant']}.toml", "\n".join(body))


